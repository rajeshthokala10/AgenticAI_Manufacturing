# ─── Primitives ────────────────────────────────────────────────────────────


def slide_bg(c, dark: bool = False):
    c.setFillColor(DARK_BG if dark else PAGE_BG)
    c.rect(0, 0, PAGE_W, PAGE_H, fill=1, stroke=0)


def brand_strip(c, slide_num: int, total: int, dark: bool = False):
    """The "HYBRID GRAPHRAG · MANUFACTURING" strip + slide-number badge.

    Mirrors the reference deck's top-of-page identity. On dark cover
    slides the strip is muted; on body slides it sits on a pale page bg.
    """
    text_color = HexColor("#94A3B8") if dark else INK_SOFT
    c.setFillColor(text_color)
    c.setFont("Helvetica-Bold", 8)
    c.drawString(36, PAGE_H - 28, "HYBRID GRAPHRAG  ·  MANUFACTURING COPILOT")

    # Slide number badge top-right (skipped on the cover)
    if slide_num > 0:
        c.setFillColor(ACCENT_COPPER if not dark else HexColor("#F59E0B"))
        c.setFont("Helvetica-Bold", 9)
        c.drawRightString(PAGE_W - 36, PAGE_H - 28, f"{slide_num:02d} / {total:02d}")


def slide_footer(c, label: str, dark: bool = False):
    color = HexColor("#94A3B8") if dark else INK_SOFT
    c.setFillColor(color)
    c.setFont("Helvetica-Oblique", 7.5)
    c.drawString(36, 18, label)
    c.drawRightString(PAGE_W - 36, 18,
                      "Generated by system_design/generate_pitch_deck.py")


def slide_title(c, num_label: str, title: str, subtitle: str):
    """Big chapter heading used by every body slide.

    Two-line: "02  THE EXPENSIVE PROBLEM" on top, italic subtitle below.
    """
    # number tab
    c.setFillColor(ACCENT_COPPER)
    c.setFont("Helvetica-Bold", 14)
    c.drawString(36, PAGE_H - 70, num_label)

    # title
    c.setFillColor(INK)
    c.setFont("Helvetica-Bold", 26)
    c.drawString(80, PAGE_H - 72, title)

    # subtitle
    c.setFillColor(INK_MID)
    c.setFont("Helvetica-Oblique", 12)
    c.drawString(36, PAGE_H - 96, subtitle)

    # horizontal rule
    c.setStrokeColor(RULE)
    c.setLineWidth(0.6)
    c.line(36, PAGE_H - 108, PAGE_W - 36, PAGE_H - 108)


def callout_card(c, x, y, w, h, *, title, value=None, body=None,
                 accent=ACCENT_COPPER, value_size=22, body_size=8.5):
    """Reusable highlight-card with a coloured top border + big stat.

    Used heavily on slides 2, 5, 8, 11. Plays the role of the reference
    deck's "$5.1M saved per month" hero numbers.
    """
    # white panel
    c.setFillColor(CARD_FILL)
    c.setStrokeColor(RULE)
    c.setLineWidth(0.5)
    c.roundRect(x, y, w, h, 6, fill=1, stroke=1)

    # top accent stripe
    c.setFillColor(accent)
    c.setStrokeColor(accent)
    c.roundRect(x, y + h - 4, w, 4, 1, fill=1, stroke=0)
    c.rect(x, y + h - 4, w, 4, fill=1, stroke=0)

    # title
    c.setFillColor(INK_MID)
    c.setFont("Helvetica-Bold", 8.5)
    c.drawString(x + 12, y + h - 20, title.upper())

    # big value
    if value:
        c.setFillColor(INK)
        c.setFont("Helvetica-Bold", value_size)
        c.drawString(x + 12, y + h - 20 - value_size - 4, value)

    # body lines
    if body:
        c.setFillColor(INK_MID)
        c.setFont("Helvetica", body_size)
        line_y = (y + h - 20 - (value_size + 18 if value else 0))
        if value:
            line_y -= 6
        for line in body:
            c.drawString(x + 12, line_y, line)
            line_y -= body_size + 3


def table(c, x, y, headers, rows, col_widths, *,
          row_h=20, header_h=24,
          header_font_size=9, body_font_size=9,
          highlight_col=None, highlight_color=ACCENT_COPPER):
    """Reference-deck-style table.

    ``highlight_col`` shades the "winning column" (Hybrid GraphRAG) with a
    light tint so the visual reading order is correct. Returns the bottom
    y-coordinate so callers can stack more content below.
    """
    total_w = sum(col_widths)

    # header row
    cur_y = y - header_h
    c.setFillColor(TABLE_HEADER_BG)
    c.rect(x, cur_y, total_w, header_h, fill=1, stroke=0)

    if highlight_col is not None:
        hx = x + sum(col_widths[:highlight_col])
        hw = col_widths[highlight_col]
        c.setFillColor(highlight_color)
        c.rect(hx, cur_y, hw, header_h, fill=1, stroke=0)

    c.setFillColor(white)
    c.setFont("Helvetica-Bold", header_font_size)
    cx = x
    for hdr, w in zip(headers, col_widths):
        c.drawString(cx + 8, cur_y + 8, str(hdr))
        cx += w

    # body rows
    for i, row in enumerate(rows):
        cur_y -= row_h
        if i % 2 == 0:
            c.setFillColor(TABLE_ALT_ROW)
            c.rect(x, cur_y, total_w, row_h, fill=1, stroke=0)
        if highlight_col is not None:
            hx = x + sum(col_widths[:highlight_col])
            hw = col_widths[highlight_col]
            tint = HexColor("#FEF3C7") if i % 2 == 0 else HexColor("#FDE68A")
            c.setFillColor(tint)
            c.rect(hx, cur_y, hw, row_h, fill=1, stroke=0)
        c.setFillColor(INK)
        c.setFont("Helvetica", body_font_size)
        cx = x
        for j, (cell, w) in enumerate(zip(row, col_widths)):
            # bold the highlight column body
            if highlight_col is not None and j == highlight_col:
                c.setFont("Helvetica-Bold", body_font_size)
            else:
                c.setFont("Helvetica", body_font_size)
            text = str(cell)
            # hard-truncate per column to avoid overflow
            max_chars = int((w - 16) / (body_font_size * 0.55))
            if len(text) > max_chars:
                text = text[: max_chars - 1] + "\u2026"
            c.drawString(cx + 8, cur_y + 6, text)
            cx += w

    # outer border
    c.setStrokeColor(RULE)
    c.setLineWidth(0.5)
    c.rect(x, cur_y, total_w, y - cur_y, fill=0, stroke=1)
    return cur_y


def chip(c, x, y, w, h, label, color, light=False):
    """Pill-shaped tag used on slides 8/9/10."""
    fill = color if not light else HexColor("#FFFFFF")
    text_color = white if not light else color
    c.setFillColor(fill)
    c.setStrokeColor(color)
    c.setLineWidth(1)
    c.roundRect(x, y, w, h, h / 2, fill=1, stroke=1)
    c.setFillColor(text_color)
    c.setFont("Helvetica-Bold", 8)
    c.drawCentredString(x + w / 2, y + h / 2 - 3, label)


def arrow_horizontal(c, x1, y, x2, color=INK_MID, dashed=False, width=1.2):
    c.setStrokeColor(color)
    c.setFillColor(color)
    c.setLineWidth(width)
    if dashed:
        c.setDash([4, 3], 0)
    c.line(x1, y, x2, y)
    c.setDash([], 0)
    head = 7
    p = c.beginPath()
    p.moveTo(x2, y)
    p.lineTo(x2 - head, y + head / 2)
    p.lineTo(x2 - head, y - head / 2)
    p.close()
    c.drawPath(p, fill=1, stroke=0)


