# ─── 16:9 page size ─────────────────────────────────────────────────────────
# Match the reference deck's 13.33" × 7.5" widescreen frame so a customer
# can drop the PDF straight into a slide projector or Keynote import.

PAGE_W = 13.33 * 72  # 960 pt
PAGE_H = 7.50 * 72   # 540 pt
PAGE_SIZE = (PAGE_W, PAGE_H)


