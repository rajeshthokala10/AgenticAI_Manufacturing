"""Generate the Manufacturing Hybrid GraphRAG pitch deck (PDF).

A 12-slide landscape 16:9 deck for plant-leadership audiences. The visual
language tracks GraphRAG_Pitch_v2.pptx — dark navy headers, copper / amber
accents, sparse text, big numbers, comparison tables — but the content is
rewritten end-to-end for a manufacturing plant company:

  Slide 1  — Cover  (title + tagline + working-PoC stamp)
  Slide 2  — The expensive problem on the plant floor
  Slide 3  — Why standard AI fails on the plant floor (comparison)
  Slide 4  — Our solution: what is working today
  Slide 5  — How it works (the answer-with-evidence pipeline)
  Slide 6  — System architecture (layered stack)
  Slide 7  — Maker-checker approvals + document ACLs
  Slide 8  — Plant data sources we already ingest
  Slide 9  — Extensibility: agentic connectors to ERP / SAP / MES / DB
  Slide 10 — Concrete plant use cases
  Slide 11 — Business impact / ROI projection
  Slide 12 — The 10-week pilot plan + decision ask

Run from the repo root:

    python system_design/generate_pitch_deck.py

Output:

    system_design/manufacturing_pitch_deck.pdf
"""

